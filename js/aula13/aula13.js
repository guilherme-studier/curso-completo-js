//teste lógico com while
console.log('Como trabalhar com While')
var a = 1
while(a <= 10){
    console.log(`Passo ${a}`)
    a++
}

//teste lógico com do while
console.log('Como trabalhar com Do While')
var b = 1
do {
    console.log(`Passo ${b}`)
    b++
} while (b <= 10)

//teste lógico com for
console.log('Como trabalhar com for')
for(c = 1; c <= 10; c++){
    console.log(`Passo ${c}`)
}